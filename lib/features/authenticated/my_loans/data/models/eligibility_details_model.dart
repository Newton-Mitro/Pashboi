import 'package:pashboi/features/authenticated/my_loans/domain/entities/eligibility_details_entity.dart';

class EligibilityDetailModel extends EligibilityDetailEntity {
  EligibilityDetailModel({
    required super.id,
    required super.accountId,
    required super.depositAccountNo,
    required super.personId,
    required super.fullName,
    required super.personMembership,
    required super.balance,
    required super.withdrawableBalance,
    required super.interestRate,
    required super.isSuretyAccount,
    required super.accTypeCode,
    required super.accountTypeName,
    required super.hasCertificate,
    required super.suretyTypeCode,
    required super.minimumInstallment,
    required super.isEligible,
    required super.collareralEligible,
    required super.maximumLoanAmount,
    required super.productInterestRate,
    required super.numberOfInstallment,
    required super.totalApplyLoan,
    required super.loanableBalance,
    required super.partialApplyLoan,
    required super.isFamilyDefaulter,
    required super.isSelfDefaulter,
    required super.isCertificateSubmitted,
  });

  factory EligibilityDetailModel.fromJson(Map<String, dynamic> json) {
    return EligibilityDetailModel(
      id: json['AccountId'] as int,
      accountId: json['AccountId'] as int,
      depositAccountNo: (json['DepositAccountNo'] ?? '').toString(),
      personId: json['PersonId'] as int,
      fullName: (json['FullName'] ?? '').toString(),
      personMembership: (json['PersonMembership'] ?? '').toString(),
      balance: (json['Balance'] as num).toDouble(),
      withdrawableBalance: (json['WithdrawableBalance'] as num).toDouble(),
      interestRate: (json['InterestRate'] as num).toDouble(),
      isSuretyAccount: json['IsSuretyAccount'] as bool,
      accTypeCode: (json['AccTypeCode'] ?? '').toString(),
      accountTypeName: (json['AccountTypeName'] ?? '').toString(),
      hasCertificate: json['HasCertificate'] as bool,
      suretyTypeCode: (json['SuretyTypeCode'] ?? '').toString(),
      minimumInstallment: (json['MinimumInstallment'] ?? '').toString(),
      isEligible: json['IsEligible'] as bool,
      collareralEligible: json['CollareralEligible'] as bool,
      maximumLoanAmount: (json['MaximumLoanAmount'] as num).toDouble(),
      productInterestRate: (json['ProductInterestRate'] as num).toDouble(),
      numberOfInstallment: json['NumberOfInstallment'] as int,
      totalApplyLoan: (json['TotalApplyLoan'] as num).toDouble(),
      loanableBalance: (json['LoanableBalance'] as num).toDouble(),
      partialApplyLoan: (json['PartialApplyLoan'] as num).toDouble(),
      isFamilyDefaulter: json['IsFamilyDefaulter'] as bool,
      isSelfDefaulter: json['IsSelfDefaulter'] as bool,
      isCertificateSubmitted: json['IsCertificateSubmitted'] as bool,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      "AccountId": accountId,
      "DepositAccountNo": depositAccountNo,
      "PersonId": personId,
      "FullName": fullName,
      "PersonMembership": personMembership,
      "Balance": balance,
      "WithdrawableBalance": withdrawableBalance,
      "InterestRate": interestRate,
      "IsSuretyAccount": isSuretyAccount,
      "AccTypeCode": accTypeCode,
      "AccountTypeName": accountTypeName,
      "HasCertificate": hasCertificate,
      "SuretyTypeCode": suretyTypeCode,
      "MinimumInstallment": minimumInstallment,
      "IsEligible": isEligible,
      "CollareralEligible": collareralEligible,
      "MaximumLoanAmount": maximumLoanAmount,
      "ProductInterestRate": productInterestRate,
      "NumberOfInstallment": numberOfInstallment,
      "TotalApplyLoan": totalApplyLoan,
      "LoanableBalance": loanableBalance,
      "PartialApplyLoan": partialApplyLoan,
      "IsFamilyDefaulter": isFamilyDefaulter,
      "IsSelfDefaulter": isSelfDefaulter,
      "IsCertificateSubmitted": isCertificateSubmitted,
    };
  }
}
